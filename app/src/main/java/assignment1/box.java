package assignment1;

public class box {
    int h, w;
    box(int h, int w) {
        this.h = h;
        this.w = w;
    }

    public void setH(int h) {
        this.h = h;
    }

    public void setW(int w) {
        this.w = w;
    }

    public int getH() {
        return h;
    }

    public int getW() {
        return w;
    }


}
