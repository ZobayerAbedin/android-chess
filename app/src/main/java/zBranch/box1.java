package zBranch;

public class box1 {
    int h, w, l;

    public box1(int h, int w, int l) {
        this.h = h;
        this.w = w;
        this.l = l;
    }

    public int getH() {
        return h;
    }

    public int getW() {
        return w;
    }

    public int getL() {
        return l;
    }

    public void setH(int h) {
        this.h = h;
    }

    public void setW(int w) {
        this.w = w;
    }

    public void setL(int l) {
        this.l = l;
    }
}
